/**
 * Import drink photos from docs/drink-images/drink_images/ (e.g. <slug>.jpg)
 * into bundled assets: assets/images/drinks/<slug>.webp
 *
 * - Fits the full photo inside 640×640 with fit=contain (no crop; letterbox on #07020F).
 * - Regenerates assets/images/drinks/index.ts and merges drink-image-gradients.json for known slugs.
 *
 * Usage: node scripts/drink-images/apply-docs-upload.mjs
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import sharp from 'sharp';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..', '..');
const DOCS_IN = path.join(ROOT, 'docs', 'drink-images', 'drink_images');
const DRINK_IMG_DIR = path.join(ROOT, 'assets', 'images', 'drinks');
const RECIPES = path.join(ROOT, 'src', 'data', 'recipes.json');
const GRADIENTS_OUT = path.join(ROOT, 'src', 'data', 'drink-image-gradients.json');
const INDEX_OUT = path.join(DRINK_IMG_DIR, 'index.ts');

const MAX_EDGE = 640;
const WEBP_QUALITY = 82;
const IMAGE_EXT = new Set(['.png', '.jpg', '.jpeg', '.webp']);
/** Letterbox to match app void so bars blend with card chrome (no crop). */
const FIT_BG = { r: 7, g: 2, b: 15, alpha: 1 };

function rgbToHex(r, g, b) {
  return (
    '#' +
    [r, g, b]
      .map((x) => Math.max(0, Math.min(255, Math.round(x))).toString(16).padStart(2, '0'))
      .join('')
  );
}

function luminance(hex) {
  const n = hex.slice(1);
  const r = parseInt(n.slice(0, 2), 16);
  const g = parseInt(n.slice(2, 4), 16);
  const b = parseInt(n.slice(4, 6), 16);
  return (0.299 * r + 0.587 * g + 0.114 * b) / 255;
}

function darkenHex(hex, factor) {
  const n = hex.slice(1);
  const r = parseInt(n.slice(0, 2), 16) * factor;
  const g = parseInt(n.slice(2, 4), 16) * factor;
  const b = parseInt(n.slice(4, 6), 16) * factor;
  return rgbToHex(r, g, b);
}

async function extractGradient(imagePath) {
  const { data, info } = await sharp(imagePath)
    .resize(56, 56)
    .raw()
    .toBuffer({ resolveWithObject: true });

  const ch = info.channels;
  const buckets = new Map();

  for (let i = 0; i < data.length; i += ch) {
    const r = data[i];
    const g = data[i + 1];
    const b = data[i + 2];
    const lum = 0.299 * r + 0.587 * g + 0.114 * b;
    if (lum < 22 || lum > 248) continue;
    const qr = (r >> 3) << 3;
    const qg = (g >> 3) << 3;
    const qb = (b >> 3) << 3;
    const key = `${qr},${qg},${qb}`;
    buckets.set(key, (buckets.get(key) ?? 0) + 1);
  }

  const sorted = [...buckets.entries()].sort((a, b) => b[1] - a[1]);
  const toHex = (entry) => {
    const [key] = entry;
    const [rs, gs, bs] = key.split(',').map(Number);
    return rgbToHex(rs, gs, bs);
  };

  const top = sorted.slice(0, 6).map(toHex);
  if (top.length === 0) {
    return {
      gradient: ['#07020F', '#1A1230'],
      accent: '#FF2E93',
    };
  }

  let c1 = top[0];
  let c2 = top[1] ?? top[0];
  if (c1 === c2 && top[2]) c2 = top[2];

  if (luminance(c1) > luminance(c2)) [c1, c2] = [c2, c1];
  c1 = darkenHex(c1, 0.45);
  c2 = darkenHex(c2, 0.65);
  if (luminance(c2) < 0.08) c2 = darkenHex(top[0] ?? '#1A1230', 0.55);

  const accent = top.find((h) => h !== c1 && h !== c2) ?? '#00E5FF';

  return { gradient: [c1, c2], accent };
}

function listCanonicalRasterSlugs(slugSet) {
  const slugs = new Set();
  if (!fs.existsSync(DRINK_IMG_DIR)) return [];
  for (const name of fs.readdirSync(DRINK_IMG_DIR)) {
    if (name === 'incoming' || name === 'index.ts' || name === 'README.md' || name === '.gitkeep') continue;
    const ext = path.extname(name).toLowerCase();
    if (ext !== '.webp' && ext !== '.png') continue;
    const base = name.slice(0, -ext.length);
    if (slugSet.has(base)) slugs.add(base);
  }
  return [...slugs].sort();
}

function writeIndex(slugs) {
  const lines = [];
  for (const s of slugs) {
    const webpPath = path.join(DRINK_IMG_DIR, `${s}.webp`);
    const pngPath = path.join(DRINK_IMG_DIR, `${s}.png`);
    const ext = fs.existsSync(webpPath) ? 'webp' : fs.existsSync(pngPath) ? 'png' : null;
    if (!ext) continue;
    lines.push(`  '${s}': require('./${s}.${ext}'),`);
  }
  const body = [
    '/**',
    ' * Auto-generated by scripts/drink-images/apply-docs-upload.mjs',
    ' * Metro requires literal require() paths per slug.',
    ' */',
    '',
    'export const DRINK_IMAGES: Record<string, number> = {',
    ...lines,
    '};',
    '',
    'export function resolveDrinkImage(slug: string): number | undefined {',
    '  return DRINK_IMAGES[slug];',
    '}',
    '',
  ].join('\n');
  fs.writeFileSync(INDEX_OUT, body, 'utf8');
}

function loadJson(p, fallback) {
  if (!fs.existsSync(p)) return fallback;
  try {
    return JSON.parse(fs.readFileSync(p, 'utf8'));
  } catch {
    return fallback;
  }
}

async function main() {
  if (!fs.existsSync(DOCS_IN)) {
    console.error(`[docs-upload] Missing folder: ${path.relative(ROOT, DOCS_IN)}`);
    process.exit(1);
  }

  const rawRecipes = JSON.parse(fs.readFileSync(RECIPES, 'utf8'));
  const drinks = rawRecipes.drinks ?? [];
  const slugSet = new Set(drinks.map((d) => d.slug));

  const files = fs
    .readdirSync(DOCS_IN, { withFileTypes: true })
    .filter((e) => e.isFile())
    .map((e) => e.name)
    .filter((n) => IMAGE_EXT.has(path.extname(n).toLowerCase()));

  const unknown = [];
  const applied = [];

  fs.mkdirSync(DRINK_IMG_DIR, { recursive: true });

  for (const name of files) {
    const ext = path.extname(name).toLowerCase();
    const slug = name.slice(0, -ext.length).toLowerCase();
    if (!slugSet.has(slug)) {
      unknown.push(name);
      continue;
    }
    const srcFull = path.join(DOCS_IN, name);
    const dest = path.join(DRINK_IMG_DIR, `${slug}.webp`);

    await sharp(srcFull)
      .rotate()
      .resize(MAX_EDGE, MAX_EDGE, {
        fit: 'contain',
        position: 'centre',
        background: FIT_BG,
        withoutEnlargement: false,
      })
      .webp({ quality: WEBP_QUALITY, effort: 5 })
      .toFile(dest);

    const legacyPng = path.join(DRINK_IMG_DIR, `${slug}.png`);
    if (fs.existsSync(legacyPng)) fs.unlinkSync(legacyPng);
    applied.push(slug);
  }

  let gradients = loadJson(GRADIENTS_OUT, {});
  if (typeof gradients !== 'object' || gradients === null) gradients = {};

  const slugs = listCanonicalRasterSlugs(slugSet);
  for (const s of slugs) {
    const rasterPath = path.join(DRINK_IMG_DIR, `${s}.webp`);
    if (!fs.existsSync(rasterPath)) continue;
    gradients[s] = await extractGradient(rasterPath);
  }

  for (const key of Object.keys(gradients)) {
    if (!slugSet.has(key)) delete gradients[key];
  }

  fs.writeFileSync(GRADIENTS_OUT, JSON.stringify(gradients, null, 2) + '\n', 'utf8');
  writeIndex(slugs);

  console.log(`[docs-upload] applied ${applied.length} image(s) from ${path.relative(ROOT, DOCS_IN)}`);
  if (unknown.length) {
    console.warn(`[docs-upload] skipped ${unknown.length} file(s) (no matching recipe slug), e.g.: ${unknown.slice(0, 8).join(', ')}`);
  }
  console.log(`[docs-upload] bundled slugs in index: ${slugs.length}`);
  console.log(`[docs-upload] wrote ${path.relative(ROOT, INDEX_OUT)}`);
  console.log(`[docs-upload] wrote ${path.relative(ROOT, GRADIENTS_OUT)}`);
}

await main().catch((e) => {
  console.error(e);
  process.exit(1);
});
